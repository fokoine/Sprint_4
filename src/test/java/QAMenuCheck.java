import org.junit.Test;
import ru.praktikum.MainPageObj;

import static org.hamcrest.CoreMatchers.containsString;
import static org.hamcrest.MatcherAssert.assertThat;

public class QAMenuCheck extends TestPreloadData {
    @Test
    public void qaMenuCheckTest() {
        MainPageObj objFirst = new MainPageObj(driver);
        objFirst.clickCookieButton();
        // Заголовки меню по порядку
        String[] title = {
                "Сколько это стоит? И как оплатить?",
                "Хочу сразу несколько самокатов! Так можно?",
                "Как рассчитывается время аренды?",
                "Можно ли заказать самокат прямо на сегодня?",
                "Можно ли продлить заказ или вернуть самокат раньше?",
                "Вы привозите зарядку вместе с самокатом?",
                "Можно ли отменить заказ?",
                "Я жизу за МКАДом, привезёте?"
        };
        // Содержимое меню по порядку
        String[] inner = {
                "Сутки — 400 рублей. Оплата курьеру — наличными или картой.",
                "Пока что у нас так: один заказ — один самокат. Если хотите покататься с друзьями, можете просто сделать несколько заказов — один за другим.",
                "Допустим, вы оформляете заказ на 8 мая. Мы привозим самокат 8 мая в течение дня. Отсчёт времени аренды начинается с момента, когда вы оплатите заказ курьеру. Если мы привезли самокат 8 мая в 20:30, суточная аренда закончится 9 мая в 20:30.",
                "Только начиная с завтрашнего дня. Но скоро станем расторопнее.",
                "Пока что нет! Но если что-то срочное — всегда можно позвонить в поддержку по красивому номеру 1010.",
                "Самокат приезжает к вам с полной зарядкой. Этого хватает на восемь суток — даже если будете кататься без передышек и во сне. Зарядка не понадобится.",
                "Да, пока самокат не привезли. Штрафа не будет, объяснительной записки тоже не попросим. Все же свои.",
                "Да, обязательно. Всем самокатов! И Москве, и Московской области."
        };
        for (int j = 0 ; j < inner.length; j++) {
            assertThat(objFirst.checkQAMenuContentTitle(j), containsString(title[j]));
            objFirst.checkQAMenu(j);
            assertThat(objFirst.checkQAMenuContentInnerContent(j), containsString(inner[j]));
        }
    }
}
